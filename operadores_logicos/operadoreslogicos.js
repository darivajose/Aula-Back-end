let numero1 = 10
let numero2 = 20

// Operadores lógicos   

// AND (&&) - Retorna true se ambos os operandos forem verdadeiros
console.log(numero1 > 5 && numero2 > 15) // true

// OR (||) - Retorna true se pelo menos um dos operandos for verdadeiro
console.log(numero1 > 5 || numero2 < 15) // true

// NOT (!) - Retorna true se o operando for falso
console.log(!(numero1 < 5)) // true 

// Exemplo com variáveis booleanas
let isAdult = true
let hasID = true

// AND
console.log(isAdult && hasID) // true

console.log(numero1 != numero2);
